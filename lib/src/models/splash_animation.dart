/// The type of animation to apply to the splash screen elements.
enum SplashAnimation {
  /// A simple fade in transition.
  fade,

  /// Scales the element from 0 to 1.
  scale,

  /// Slides the element up from the bottom.
  slideUp,

  /// Slides the element down from the top.
  slideDown,

  /// Slides the element in from the left.
  slideLeft,

  /// Slides the element in from the right.
  slideRight,

  /// Rotates the element.
  rotate,

  /// Zooms in towards the element.
  zoomIn,

  /// Zooms out from the element.
  zoomOut,

  /// Bounces the element into place.
  bounce,

  /// Uses an elastic spring-like animation.
  elastic,

  /// Creates a ripple effect from the center.
  ripple,

  /// Pulses the element continuously.
  pulse,

  /// Creates a wave-like animation.
  wave,

  /// Applies a shimmering effect across the element.
  shimmer,

  /// Advanced: Fireworks particle explosion.
  fireworks,

  /// Advanced: Confetti raining down.
  confetti,

  /// Advanced: Particles floating around.
  particles,

  /// Advanced: Elements floating up and down gently.
  floating,

  /// Advanced: Elements orbiting around the center.
  orbit,

  /// Advanced: Continuous spinning animation.
  spin,

  /// Advanced: Glowing pulsing effect.
  glow,

  /// Advanced: Morphing shapes.
  morph,

  /// Advanced: Liquid filling effect.
  liquid,

  /// Advanced: Reveal effect.
  reveal,
}
